    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        String name = nameTextField.getText();
        Type type3 = (Type) organizationJComboBox.getSelectedItem();
        if(type3.getValue().equals("Director Department")){
            for(Organization org : directory.getOrganizationList()){
                if(org.getType().equals("Director Department")){
                    JOptionPane.showMessageDialog(null, "Only one director department can be created!");
                    return;
                }
            }
        }
        if(type3.getValue().equals("DataAnalyst Department")){
            for(Organization org : directory.getOrganizationList()){
                if(org.getType().equals("DataAnalyst Department")){
                    JOptionPane.showMessageDialog(null, "Only one data analyst department can be created!");
                    return;
                }
            }
        }
        if(type3.getValue().equals("Publisher Department")){
            for(Organization org : directory.getOrganizationList()){
                if(org.getType().equals("Publisher Department")){
                    JOptionPane.showMessageDialog(null, "Only one publisher department can be created!");
                    return;
                }
            }
        }
        directory.createOrganization(type3,name);
        JOptionPane.showMessageDialog(null, "A new department has been created");
        populateTable();
    }//GEN-LAST:event_addJButtonActionPerformed


