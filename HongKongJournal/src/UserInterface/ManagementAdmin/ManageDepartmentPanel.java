    private void populateCombo(){
        organizationJComboBox.removeAllItems();
        switch (type) {
            case "Strategy":
                for(Type type1 : Organization.Type.values()){
                    if(type1.getValue().equals(Type.Director.getValue()) || type1.getValue().equals(Type.DataAnalyst.getValue()))
                        // publisher type?
                        organizationJComboBox.addItem(type1);
                }   break;
            case "Press":
                for(Type type2 : Organization.Type.values()){
                    if(type2.getValue().equals(Type.Publisher.getValue()))
                        // publisher type?
                        organizationJComboBox.addItem(type2);
            }   break;
        }
    }
    
    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : directory.getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization;
            row[1] = organization.getType();
            
            model.addRow(row);
        }
    }

